
// When we pass concrete references to longest, the concrete lifetime 
// that is substituted for 'a is the part of the scope of x that overlaps
// with the scope of y. In other words, the generic lifetime 'a will get
// the concrete lifetime that is equal to the smaller of the lifetimes of x and y. 
// Because weâ€™ve annotated the returned reference with the same lifetime 
// parameter 'a, the returned reference will also be valid for the 
// length of the smaller of the lifetimes of x and y.
fn longest<'a>(x: &'a str, y: &'a str) -> &'a str {
    if x.len() > y.len() {
        x
    } else {
        y
    }
}

fn example_1() {
    let string1 = String::from("abcd");
    let string2 = "xyz";

    let result = longest(string1.as_str(), string2);
    println!("The longest string is {}", result);
}

fn example_2() {
    let string1 = String::from("long string is long");

    {
        let string2 = String::from("xyz");
        let result = longest(string1.as_str(), string2.as_str());
        println!("The longest string is {}", result);
    }
}

